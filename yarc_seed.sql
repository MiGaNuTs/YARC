-- YARC - Yet Another Runeword Calculator
-- Seed data generated from blizzhackers/d2data

CREATE TABLE IF NOT EXISTS runes (
    id       INTEGER PRIMARY KEY,
    name_key TEXT NOT NULL,
    name_en  TEXT NOT NULL,
    clvl     INTEGER NOT NULL
);

CREATE TABLE IF NOT EXISTS rune_effects (
    id        INTEGER PRIMARY KEY,
    name_key  TEXT NOT NULL,
    item_type TEXT NOT NULL,
    effect_en TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS item_types (
    id      INTEGER PRIMARY KEY,
    code    TEXT NOT NULL UNIQUE,
    name_en TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS runewords (
    id            INTEGER PRIMARY KEY,
    name_key      TEXT NOT NULL,
    name_en       TEXT NOT NULL,
    runes_display TEXT NOT NULL,
    rune1_id      INTEGER NOT NULL,
    rune2_id      INTEGER NOT NULL,
    rune3_id      INTEGER,
    rune4_id      INTEGER,
    rune5_id      INTEGER,
    rune6_id      INTEGER,
    sockets       INTEGER NOT NULL,
    clvl          INTEGER NOT NULL,
    patch_min     INTEGER NOT NULL,
    patch_max     INTEGER,
    season_min    INTEGER,
    season_max    INTEGER,
    FOREIGN KEY (rune1_id) REFERENCES runes(id),
    FOREIGN KEY (rune2_id) REFERENCES runes(id),
    FOREIGN KEY (rune3_id) REFERENCES runes(id),
    FOREIGN KEY (rune4_id) REFERENCES runes(id),
    FOREIGN KEY (rune5_id) REFERENCES runes(id),
    FOREIGN KEY (rune6_id) REFERENCES runes(id)
);

CREATE TABLE IF NOT EXISTS runeword_effects (
    id        INTEGER PRIMARY KEY,
    name_key  TEXT NOT NULL,
    effect_en TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS runeword_item_types (
    runeword_id  INTEGER NOT NULL,
    item_type_id INTEGER NOT NULL,
    PRIMARY KEY (runeword_id, item_type_id),
    FOREIGN KEY (runeword_id)  REFERENCES runewords(id),
    FOREIGN KEY (item_type_id) REFERENCES item_types(id)
);

CREATE TABLE IF NOT EXISTS translations (
    name_key TEXT NOT NULL,
    lang     TEXT NOT NULL,
    value    TEXT NOT NULL,
    PRIMARY KEY (name_key, lang)
);

